public class Country {
    String countryName;
    String countryAb;


    public Country(String countryName, String countryAb) {
        this.countryName = countryName;
        this.countryAb = countryAb;
    }

    public String getCountryName() {
        return countryName;
    }

    public String getCountryAb() {
        return countryAb;


    }
}
